#include <iostream>
using namespace std;
int main()
{
    short int x, y, z;

    cout << "Enter Value of X?" << endl;
    cin >> x;
    cout << "Enter Value of Y?" << endl;
    cin >> y;
    cout << "Enter Value of Z?" << endl;
    cin >> z;

    cout << x << " +\n";
    cout << y << " +\n";
    cout << z << "\n";
    cout << "__________________\n" << endl;
    cout << "Total = " << x + y + z << endl;


}

